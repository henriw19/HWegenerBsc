import numpy as np

def square(x: int) -> int:
    """
    Return the square of an integer.

    Parameters
    ----------
    x : int
        The integer to be squared.

    Returns
    -------
    int
        The square of the input integer.
    """
    return x*x

def cube(x: int) -> int:
    """
    Return the cube of an integer.

    Parameters
    ----------
    x : int
        The integer to be cubed.

    Returns
    -------
    int
        The cube of the input integer.
    """
    return x**3
